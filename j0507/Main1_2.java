package j0507;

public class Main1_2 {
    public static void main(String[] args) {
        // 3を変数aに入れろ
        int a = 3;

        // 5を変数bに入れろ
        int b = 5;

        // その掛け算の結果を変数cに入れろ
        int c = a * b;

        // 変数cを表示せよ
        // 縦幅3横幅5の長方形の面積は、15
        System.out.println("縦幅" + a + "横幅" + b + "の長方形の面積は、" + c);
    }
}
